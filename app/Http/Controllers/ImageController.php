    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $item = Images::find($id);
        @unlink('storage/'.$item->file);
        $item->delete();
        return redirect()->route('images.index');
    }
